
forall <- kiwi:::forall
test_cases <- kiwi:::test_cases

require(kiwi)

message('xToChars')

	forall(
		"chars of the empty string is an empty vector",
		list(),
		xToChars('') %is% character(0)
	)

	forall(
		"chars of a single letter is identity",
		test_cases$letter,
		xToChars(str) == str
	)

	forall(
		"length of chars is nchars of a string",
		test_cases$str_word,
		length(xToChars(str)) == nchar(str)
	)

